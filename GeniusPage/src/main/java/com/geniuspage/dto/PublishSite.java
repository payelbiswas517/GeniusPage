package com.geniuspage.dto;

public class PublishSite {

	private String publishingOption;
	private String siteName;
	private String sitePublishName;
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	public String getPublishingOption() {
		return publishingOption;
	}
	public void setPublishingOption(String publishingOption) {
		this.publishingOption = publishingOption;
	}
	public String getSitePublishName() {
		return sitePublishName;
	}
	public void setSitePublishName(String sitePublishName) {
		this.sitePublishName = sitePublishName;
	}
	
}
